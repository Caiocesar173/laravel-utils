        Schema::disableForeignKeyConstraints();
        DB::table('countries')->truncate();
        $counter = 0;
        foreach ($countries as $country) 
        {
            $continentName = $country["continent"];
            $continent = Continent::where('name', $continentName)->first();

            if(is_null($continent))
                throw new \Exception("unable to find continent: $continentName", 1);
            
            $country['continent'] = $continent->id; 
            $save = Country::create($country);
            if (is_string($save))
                throw new \Exception($save, 1);
            
            if($this->command)                
                $this->command->info("Inserted country {$country['name']} from continent $continent->name [$counter].\n");
            else
                echo("Inserted country {$country['name']} from continent $continent->name [$counter].\n");

            $counter += 1;
        }
        Schema::enableForeignKeyConstraints();
    }
